/**
 * Calcute and return the addition of two numbers
 * @param {number} num1 
 * @param {number} num2 
 * @returns the result 
 */
function add(num1,num2)
{
    return num1+num2;
}

/**
 * This function calculates and returns the difference of two numbers
 * @param {number} number1 
 * @param {number} number2 
 * @returns result of number1 - number2
 */
function subtract(number1, number2){
    return number1 - number2;
}
/**
 * number and power are the input and it will return the output
 * @param {*} number
 * @param {*} power
 * @returns 
 */
function powers(number,power){
    return number**power
}
/**
 * Multiply num1 and num2
 * @param {number} num1 
 * @param {number} num2 
 * @returns the result
 */
function multiply(num1,num2)
{
    return num1*num2;
}